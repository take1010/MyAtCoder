const int MAX = 2000001;
long long fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

int main(){
	int n,m,k;
	cin>>n>>m>>k;
	if(n>m){
		cout<<0<<endl;
		return 0;
	}
	COMinit();
	int ans=COM(m+n,n);
	int tmp1=n-k-1;
	int tmp2=m;
	int tmp3;
	
	while(tmp1>=0 && tmp2>=0){
		tmp3 = COM(tmp1+tmp2, tmp2);
		// cout<<"tmp: "<<tmp1<<" "<<tmp2<<" "<<tmp3<<endl;
		ans = (ans+MOD-tmp3)%MOD;
		tmp1--;
		tmp2--;
	}
	cout<<ans<<endl;
	return 0;
}